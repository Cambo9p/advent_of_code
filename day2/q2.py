def psr_strat(input: str) -> int: 
    """returns the score from the given rps game in question 2"""
    score: int = 0
    rounds: list = input.splitlines()
    game: str
    _win: int = 6
    _draw: int = 3 
    _loss: int = 0
    choose_win = 'Z'
    choose_draw = 'Y'
    choose_loss = 'X'

    win: dict = {'A':'P', 'B':'S', 'C':'R'} # maps opponent choice to your choice for you to win
    loss: dict = {'A':'S', 'B':'R', 'C':'P'} # maps opponent to choice to your choice for you to lose 
    draw: dict = {'A':'R', 'B':'P', 'C':'S'}
    choice_to_score: dict = {'R':1, 'P':2, 'S':3}

    for round in rounds:
        game = round.split(' ')
        opponent = game[0]
        move = game[1] # either win lose or draw 
        choice: str

        # first determine your choice 
        if (move == choose_win):
            choice = win[opponent]
            score += _win
        elif (move == choose_loss):
            choice = loss[opponent]
            score += _loss
        else:
            choice = draw[opponent]
            score += _draw

        # add the score by your choice 
        score += choice_to_score[choice]
        # add the points for w/l/d to the score 

    return score

